import pandas as pd
import matplotlib.pyplot as plt
from sklearn.linear_model import LinearRegression
import numpy as np

# Exemple : X et y
data = pd.DataFrame({
    "X": [1, 2, 3, 4, 5],
    "y": [2, 4, 5, 4, 5]
})

X = data[["X"]]
y = data["y"]

# Mod√®le
model = LinearRegression()
model.fit(X, y)

# Pr√©diction pour tracer la droite
X_range = np.linspace(X.min(), X.max(), 100).reshape(-1, 1)
y_pred = model.predict(X_range)

# Graphique
plt.scatter(X, y)
plt.plot(X_range, y_pred)
plt.xlabel("X")
plt.ylabel("y")
plt.title("R√©gression Lin√©aire")
plt.show()

import pandas as pd
import matplotlib.pyplot as plt
from sklearn.linear_model import LogisticRegression
import numpy as np

# Exemple : X et y binaire
data = pd.DataFrame({
    "X": [1, 2, 3, 4, 5, 6],
    "y": [0, 0, 0, 1, 1, 1]
})

X = data[["X"]]
y = data["y"]

# Mod√®le
model = LogisticRegression()
model.fit(X, y)

# Courbe logistique
X_range = np.linspace(X.min(), X.max(), 300).reshape(-1, 1)
y_prob = model.predict_proba(X_range)[:, 1]

# Graphique
plt.scatter(X, y)
plt.plot(X_range, y_prob)
plt.xlabel("X")
plt.ylabel("Probabilit√©")
plt.title("R√©gression Logistique")
plt.show()

